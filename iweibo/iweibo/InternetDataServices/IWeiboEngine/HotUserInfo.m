#import "HotUserInfo.h"


@implementation HotUserInfo

@synthesize userName;
@synthesize userhead;
@synthesize numberOfFans;
@synthesize hasListen;
@synthesize headURL;
@synthesize is_auth;
@synthesize userNick;
@synthesize local_id;

- (void)dealloc {
	
	[userName release];
	[userNick release];
	[userhead release];
	[numberOfFans release];
	[hasListen release];
	[headURL release];
	[is_auth release];
	[local_id release];
	
	[super dealloc];
}
@end
