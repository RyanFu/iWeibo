/**
 * emoji表情处理模块
 * @author lololi
 *
 */
class Core_Comm_Emoji
{
    const MAX_LEN = 4;
    public static $emojiMap = array();    //emoji表情文字与unicode码的对照表
    
    /**
     * 处理微博内容中的emoji表情，如/z可爱
     * 使得iPhone客户端可以直接显示emoji表情
     * @param string $content
     */
    public static function formatEmoji($content)
    {
        $len = self::MAX_LEN;
        for(;$len>0;$len--)
        {
            $pattern = '/\/z[^\/]{'.$len.'}/u';
            preg_match_all($pattern, $content, $matches);
            foreach($matches[0] as $m)
            {
                $emojiUnicode = Core_Comm_Emoji::$emojiMap[$m];
                if(!empty($emojiUnicode))
                {
                    $utf8Value = Core_Comm_Emoji::unicodeToUtf8($emojiUnicode);
                    $content = str_replace($m, $utf8Value, $content);
                }
            }
        }
        return $content;
    }

    /**
     * 将uincode字串转换成对应的UTF-8格式
     * @param string $origin
     */
    public static function unicodeToUtf8($origin)
    {
        $first = substr($origin, 0, 2);
        $fn = (int)hexdec($first);
        $second = substr($origin, 2, 2);
        $sd = (int)hexdec($second);
        $pcUtf8[0] = (0xE0 | (($fn & 0xF0) >> 4));
        $pcUtf8[1] = (0x80 | (($fn & 0x0F) << 2)) + (($sd & 0xC0) >> 6);
        $pcUtf8[2] = (0x80 | ($sd & 0x3F));
        return chr($pcUtf8[0]).chr($pcUtf8[1]).chr($pcUtf8[2]);
    }
}

